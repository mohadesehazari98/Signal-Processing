f0 =10;
Fs=16000;
Fs1=16000;
Fs2=32000;
Fs3=64000;
t = (-1000/f0) : (1/Fs) : (1000/f0);
x =( f0 * ((sinc(f0*t)).^2) )+( 15* sin(2*pi*f0*t));
x1 = x(1 : Fs/Fs1 : end);
x2 = x(1 : Fs/Fs2 : end);
x3 = x(1 : Fs/Fs3 : end);
subplot(3,1,1);
h1=stem(x1);
xlim([-Fs1/f0 Fs1/f0]);
subplot(3,1,2);
h2=stem(x2);
xlim([-Fs2/f0 Fs2/f0]);
subplot(3,1,3);
h3=stem(x3);
xlim([-Fs3/f0 Fs3/f0]);

